def GetUA(ua_list):
    return(random.choice(ua_list))


def GenHeader(ua="desktop"):
    """Make a random header.
    ua: Default set to desktop, can choose "mobile" as
        alternative.
    """
    if re.search(r"mobile|tablet", ua, re.I):
        ua = GetUA(mobile_ua)
    else:
        ua = GetUA(desktop_ua)
    x = {"User-Agent": ua,
         "Accept": "text/html,application/xhtml+xml,application/xml;q=0.9,image/webp,*/*;q=0.8",
         "Accept-Encoding": "gzip, deflate, sdch, br",
         "Accept-Language": "zh-CN,zh;q=0.8,en;q=0.6"}
    return(x)


